<?php // for, while 둘 다 반복문.


    $i= 0; // 초기화
    while($i < 10) // 조건식
    {
        print $i . "<br>"; // 내용
        $i++; // 증감식
    }

?>



<!--
for, while 둘 다 반복문.
for문 : 몇번 반복해야하는지 숫자로 알 때 사용
while문 : 언제 그만둬야 하는지 알 때 사용

사실 별 차이 없다.

- 문법
