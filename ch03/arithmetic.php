<?php // 연산
    $n1 = 8;
    $n2 = 5;

    $result = $n1 + $n2;
    print "$n1 + $n2 = $result <br>";

    $result = $n1 - $n2;
    print "$n1 - $n2 = $result <br>";

    $result = $n1 * $n2;
    print "$n1 * $n2 = $result <br>";

    $result = $n1 / $n2;
    print "$n1 / $n2 = $result <br>";
    
    $result = $n1 % $n2; // %: modulo mod(모드) 나머지 값 1...3 (ex 주로 홀짝구분 0 : 짝수 1 : 홀수)
    print "$n1 % $n2 = $result <br>";
?>

